// 반복문 for
package com.eomcs.basic.ex06;

public class Exam0410 {
  public static void main(String[] args) {
    //for (변수선언 및 초기화; 조건; 증감문) 문장;

    for (int i = 1; i <= 5; i++)
      System.out.println(i);

    // 1)변수 초기화 =>  int i = 1
    // 2)조건 => i <= 5
    // 3)문장 => System.out.print(i + " ")
    // 변수 증가문 => i++
    // 조건이 참인 동안 2~4 반복.
  }
}
